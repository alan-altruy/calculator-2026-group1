package calculator.enums;

/**
 * Enum representing the different domains of numbers that can be used in calculations.
 */
public enum NumberDomain {

    /**
     * Integer domain, representing whole numbers.
     */
    INTEGER,

    /**
     * Rational domain, representing numbers that can be expressed as a fraction of two integers.
     */
    RATIONAL,

    /**
     * Real domain, representing all real numbers, including irrational numbers.
     */
    REAL,

    /**
     * Complex domain, representing numbers that have a real part and an imaginary part.
     */
    COMPLEX
}
